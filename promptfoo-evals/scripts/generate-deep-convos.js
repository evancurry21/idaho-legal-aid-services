#!/usr/bin/env node
/**
 * generate-deep-convos.js
 *
 * Reads hand-authored thematic conversations from
 * tests/conversations-deep.src.yaml and flattens them into
 * promptfoo test entries at tests/conversations-deep.yaml.
 *
 * Key design decisions:
 *   - history is ALWAYS empty — the server-side conversation cache
 *     (keyed by conversation_id, 30-min TTL) provides real multi-turn
 *     context, so we don't duplicate it client-side.
 *   - Fresh crypto.randomUUID() per conversation per generation run,
 *     so re-running the generator avoids stale cache hits.
 *   - Auto-injects a "theme-coherence" assertion on turns 2+ using
 *     the conversation's theme_keywords array.
 *   - Per-turn assert and description are carried forward from source.
 *
 * Usage:
 *   node scripts/generate-deep-convos.js
 *   # or: npm run generate:deep
 */

const fs = require('node:fs');
const path = require('node:path');
const crypto = require('node:crypto');
const yaml = require('js-yaml');

const INPUT_FILE = path.join(__dirname, '..', 'tests', 'conversations-deep.src.yaml');
const OUTPUT_FILE = path.join(__dirname, '..', 'tests', 'conversations-deep.yaml');

// --- Read source ---
const raw = fs.readFileSync(INPUT_FILE, 'utf8');
const source = yaml.load(raw);

if (!source?.conversations || !Array.isArray(source.conversations)) {
  console.error('Expected top-level "conversations" array in', INPUT_FILE);
  process.exit(1);
}

const output = [];
let totalTurns = 0;

for (const convo of source.conversations) {
  const { id, theme_keywords, nav_urls, turns } = convo;

  if (!id || !Array.isArray(turns) || turns.length === 0) {
    console.error(`Conversation missing id or turns — skipping`);
    continue;
  }

  const conversationId = crypto.randomUUID();
  const totalInConvo = turns.length;

  for (let t = 0; t < turns.length; t++) {
    const turn = turns[t];
    const turnNumber = t + 1;
    const question = turn.question;

    if (!question) {
      console.error(`[${id}] Turn ${turnNumber} has no question — skipping`);
      continue;
    }

    const entry = {
      vars: {
        question,
        conversation_id: conversationId,
        history: [],
      },
      metadata: {
        conversationId,
        conversationName: id,
        turn: turnNumber,
        totalTurns: totalInConvo,
      },
    };

    // Description format: [convo-id] T3/8 — Description text
    const descSuffix = turn.description || `Turn ${turnNumber}`;
    entry.description = `[${id}] T${turnNumber}/${totalInConvo} — ${descSuffix}`;

    // Build assertions: start with source per-turn asserts
    const asserts = [];
    if (Array.isArray(turn.assert)) {
      asserts.push(...turn.assert);
    }

    // Auto-inject theme-coherence on turns 2+
    if (turnNumber >= 2 && Array.isArray(theme_keywords) && theme_keywords.length > 0) {
      asserts.push({
        type: 'javascript',
        value: buildThemeCoherenceJs(theme_keywords, nav_urls),
        metric: 'theme-coherence',
      });
    }

    if (asserts.length > 0) {
      entry.assert = asserts;
    }

    output.push(entry);
    totalTurns++;
  }
}

// --- Write output ---
const header = [
  '# Auto-generated by scripts/generate-deep-convos.js',
  '# Source: tests/conversations-deep.src.yaml',
  `# ${source.conversations.length} conversations, ${totalTurns} total turns`,
  '#',
  '# Do not edit by hand — re-run: npm run generate:deep',
  '',
].join('\n');

const yamlStr = yaml.dump(output, {
  lineWidth: 120,
  noRefs: true,
  quotingType: '"',
  forceQuotes: false,
});

fs.writeFileSync(OUTPUT_FILE, header + yamlStr, 'utf8');

console.log(
  `Wrote ${totalTurns} tests (${source.conversations.length} conversations) to tests/conversations-deep.yaml`
);

// --- Helpers ---

/**
 * Build a JavaScript assertion that checks the response contains
 * at least one of the theme keywords (case-insensitive), OR contains
 * meta-conversation keywords (apply, legal aid, etc.), OR matches
 * navigation URLs for the topic.
 */
function buildThemeCoherenceJs(keywords, navUrls = []) {
  const escaped = keywords.map((kw) => kw.replace(/'/g, "\\'"));
  const arrayLiteral = escaped.map((kw) => `'${kw}'`).join(', ');

  // Meta keywords that are always on-topic (navigational / service-level)
  const metaKw =
    "'apply', 'legal aid', 'hotline', 'office', 'call', 'help you', 'services', 'legal advice line'";

  // Safety keywords that are always appropriate
  const safetyKw = "'call 911', 'emergency', '988', 'crisis', 'shelter', 'hotline'";

  let js = `const kw = [${arrayLiteral}]; const meta = [${metaKw}]; const safety = [${safetyKw}]; const l = output.toLowerCase(); kw.some(k => l.includes(k)) || meta.some(k => l.includes(k)) || safety.some(k => l.includes(k))`;

  // Navigation URLs for this topic
  if (Array.isArray(navUrls) && navUrls.length > 0) {
    const urlParts = navUrls.map((u) => `'${u.replace(/'/g, "\\'")}'`).join(', ');
    js += ` || [${urlParts}].some(u => output.includes(u))`;
  }

  return js;
}
