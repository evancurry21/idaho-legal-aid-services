#!/usr/bin/env node
/**
 * generate-multiturn.js
 *
 * Reads the 50 single-turn tests from tests/live-50.sanitized.yaml
 * and converts them into 10 conversations of 5 turns each, writing
 * the output to tests/live-10convos.yaml.
 *
 * Each subsequent turn in a conversation carries the full prior
 * history (user messages only) so the live endpoint sees a growing
 * context.history array.
 *
 * Usage:
 *   node scripts/generate-multiturn.js
 *   # or: npm run generate:multiturn
 */

const fs = require('node:fs');
const path = require('node:path');
const crypto = require('node:crypto');
const yaml = require('js-yaml');

const TURNS_PER_CONVO = 5;
const INPUT_FILE = path.join(__dirname, '..', 'tests', 'live-50.sanitized.yaml');
const OUTPUT_FILE = path.join(__dirname, '..', 'tests', 'live-10convos.yaml');

// --- Read source tests ---
const raw = fs.readFileSync(INPUT_FILE, 'utf8');
const tests = yaml.load(raw);

if (!Array.isArray(tests)) {
  console.error('Expected a YAML array in', INPUT_FILE);
  process.exit(1);
}

if (tests.length < TURNS_PER_CONVO) {
  console.error(
    `Need at least ${TURNS_PER_CONVO} tests, found ${tests.length}`
  );
  process.exit(1);
}

// Use exactly as many tests as fit into full conversations
const usableCount = Math.floor(tests.length / TURNS_PER_CONVO) * TURNS_PER_CONVO;
const numConvos = usableCount / TURNS_PER_CONVO;

const output = [];

for (let c = 0; c < numConvos; c++) {
  const conversationId = crypto.randomUUID();
  const groupStart = c * TURNS_PER_CONVO;

  for (let t = 0; t < TURNS_PER_CONVO; t++) {
    const srcTest = tests[groupStart + t];
    const question = srcTest.vars?.question || srcTest.question;

    if (!question) {
      console.error(
        `Test at index ${groupStart + t} has no vars.question — skipping`
      );
      continue;
    }

    // Build history from prior turns in this conversation
    const history = [];
    for (let p = 0; p < t; p++) {
      const priorTest = tests[groupStart + p];
      const priorQ = priorTest.vars?.question || priorTest.question;
      if (priorQ) {
        history.push({ role: 'user', content: priorQ });
      }
    }

    const entry = {
      vars: {
        question,
        conversation_id: conversationId,
        history,
      },
      metadata: {
        conversationId,
        turn: t + 1,
      },
    };

    // Carry forward per-question assertions and description from source
    if (srcTest.description) entry.description = srcTest.description;
    if (srcTest.assert) entry.assert = srcTest.assert;

    output.push(entry);
  }
}

// --- Write output ---
const header = [
  '# Auto-generated by scripts/generate-multiturn.js',
  '# Source: tests/live-50.sanitized.yaml',
  `# ${numConvos} conversations x ${TURNS_PER_CONVO} turns = ${output.length} tests`,
  '#',
  '# Do not edit by hand — re-run: npm run generate:multiturn',
  '',
].join('\n');

const yamlStr = yaml.dump(output, {
  lineWidth: 120,
  noRefs: true,
  quotingType: '"',
  forceQuotes: false,
});

fs.writeFileSync(OUTPUT_FILE, header + yamlStr, 'utf8');

console.log(
  `Wrote ${output.length} tests (${numConvos} conversations x ${TURNS_PER_CONVO} turns) to tests/live-10convos.yaml`
);
